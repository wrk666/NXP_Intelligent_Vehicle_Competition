//内部使用用户无需调用
void OLED_WrDat(uint8 data)
{
	unsigned char i;
        OLED_DC(1);  
	for(i=0;i<8;i++) //发送一个八位数据 ,通过数据线的高低电平来传输数据
	{
		if((data << i) & 0x80)
		{
                        OLED_SDA(1);
		}
		else  
                  OLED_SDA(0);
                
                OLED_SCL(0);
                OLED_SCL(1);
	}
}

//内部使用用户无需调用
void OLED_Wr6Dat(uint8 data)
{
	uint8 i=6;
	
	OLED_DC(1);
	OLED_SCL(0);
	while(i--)
	{
	 	if(data&0x80){OLED_SDA(1);}
	 	else{OLED_SDA(0);}
	 	OLED_SCL(1);
	 	OLED_SCL(0);
	 	data<<=1;
	}
        
}

